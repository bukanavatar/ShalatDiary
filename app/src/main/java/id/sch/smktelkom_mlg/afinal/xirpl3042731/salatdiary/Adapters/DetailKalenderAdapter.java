package id.sch.smktelkom_mlg.afinal.xirpl3042731.salatdiary.Adapters;

public class DetailKalenderAdapter {
    private String head;
    private String desc;

    public DetailKalenderAdapter(String head, String desc) {
        this.head = head;
        this.desc = desc;
    }

    public String getHead() {
        return head;
    }

    public String getDesc() {
        return desc;
    }
}
